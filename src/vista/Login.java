    private void login(){
        if(!txtUser.getText().isEmpty() && !txtPassword.getText().isEmpty()){
            
            Ctrl_Usuario controlUsuario = new Ctrl_Usuario();
            Usuario usuario = new Usuario();
            usuario.setUsuario(txtUser.getText().trim());
            usuario.setPassword(txtPassword.getText().trim());
            
            if(controlUsuario.Login(usuario)){
                //JOptionPane.showMessageDialog(null, "Login correcto...");
                new Menu();
                this.dispose();
                
            }else{
                JOptionPane.showMessageDialog(null, "Usuario o contrasenia incorrecta", "ERROR INICIO DE SECCION", JOptionPane.ERROR_MESSAGE);
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Informacion incompleta, ingrese sus credenciales", "ERROR INICIO DE SECCION", JOptionPane.ERROR_MESSAGE);
        }
    }

